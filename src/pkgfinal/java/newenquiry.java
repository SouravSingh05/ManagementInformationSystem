    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Connection conn;
        PreparedStatement pst;
         SimpleDateFormat sdf=new SimpleDateFormat("yyyy-MM-dd");
        String xyz=sdf.format(d1.getDate());
        
        
        try{
               Class.forName("com.mysql.jdbc.Driver");

               conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/mis","root","");
               String sql;
                     sql = "Insert into enquiry(Customer_Id,Name,Company,City,Location,Address,Email,Phone,Other_Contact,Category,Sub_Category,Product,Customer_Type,Enquiry_Type,Sources,Date,Remarks) values (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)";
               
               pst = conn.prepareStatement(sql);
                pst.setString(1,jTextField1.getText());
                pst.setString(2,name.getText());
                pst.setString(3,company.getText());
                pst.setString(4,cc.getSelectedItem().toString());
                pst.setString(5,location.getText());
                pst.setString(6,address.getText());
                pst.setString(7,email.getText());
                pst.setString(8,phone.getText());
                pst.setString(9,othercontacts.getText());
                pst.setString(10,ac.getSelectedItem().toString());
                pst.setString(11,dc.getSelectedItem().toString());
                pst.setString(12,products.getText());
                pst.setString(13,customertype.getText());
                pst.setString(14,ec.getSelectedItem().toString());
                pst.setString(15,bc.getSelectedItem().toString());
                pst.setString(16,xyz);
                pst.setString(17,remarks.getText());
                pst.execute();
                
                 JOptionPane.showMessageDialog(null, "saved");
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, "PLEASE CHECK ALL THE FIELDS");
            }
   
    }//GEN-LAST:event_jButton1ActionPerformed

